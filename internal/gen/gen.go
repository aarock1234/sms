package gen

import (
	"html/template"
	"os"
	"regexp"
)

var (
	t = template.Must(template.New("textverified").Parse(`// Code generated by saucesteals/sms; DO NOT EDIT.
package {{ .Package }}

var (
	{{ range .Services }}Service{{ .Name }} = "{{ .Value }}"
	{{ end }}
)`))

	reNormalize = regexp.MustCompile(`(?i)(\W|_|service)`)
)

type Service struct {
	Name  string
	Value string
}

type Data struct {
	Services []Service
	Package  string
}

func Normalize(name string) string {
	return reNormalize.ReplaceAllString(name, "")
}

func Generate(path string, data Data) error {
	out, err := os.Create(path)
	if err != nil {
		return err
	}

	if err := t.Execute(out, data); err != nil {
		return err
	}

	return nil
}
